public class Dia {
    public static void main(String[] args) {
        String dia = "Terça Feira";
        switch (dia) {
            case "Segunda Feira":
                System.out.println("Dia da semana");
                break;
            case "Terça Feira":
                System.out.println("Dia da semaana");
            case "Quarta Feira":
                System.out.println("Dia da semana");
            default:
                break;
        }
    }
}
